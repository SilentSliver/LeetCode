using namespace std;
using json = nlohmann::json;

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    TreeNode* sortedArrayToBST(vector<int>& nums) {
		if (nums.empty()) {
			return nullptr;
		}
		size_t mid = nums.size() / 2;
		TreeNode* root = new TreeNode(nums[mid]);
		auto left_array = vector<int>(nums.begin(), nums.begin() + mid);
		root->left = sortedArrayToBST(left_array);
		auto right_array = vector<int>(nums.begin() + mid + 1, nums.end());
		root->right = sortedArrayToBST(right_array);
		return root;
    }
};

json leetcode::qubh::Solve(string input_json_values) {
	vector<string> inputArray;
	size_t pos = input_json_values.find('\n');
	while (pos != string::npos) {
		inputArray.push_back(input_json_values.substr(0, pos));
		input_json_values = input_json_values.substr(pos + 1);
		pos = input_json_values.find('\n');
	}
	inputArray.push_back(input_json_values);

	Solution solution;
	vector<int> nums = json::parse(inputArray.at(0));
	return TreeNodeToJsonArray(solution.sortedArrayToBST(nums));
}
